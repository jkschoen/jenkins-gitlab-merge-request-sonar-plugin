package jenkins.plugins.sonarparser.models;

/**
 *
 * @author jacob.schoen@ge.com
 */
public class SonarComponent {

    private String component;

    public SonarComponent() {
    }

    public SonarComponent(String component) {
        this.component = component;
    }

    public String getComponent() {
        return component;
    }

    public void setComponent(String component) {
        this.component = component;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 97 * hash + (this.component != null ? this.component.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final SonarComponent other = (SonarComponent) obj;
        return !((this.component == null) ? (other.component != null) : !this.component.equals(other.component));
    }

    @Override
    public String toString() {
        return "SonarComponent{" + "component=" + component + '}';
    }

}
